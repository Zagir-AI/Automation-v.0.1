def get_simultaneous_factor(n: int) -> float:
    """
    Коэффициент одновременности (участия в максимуме) = 1.0.

    По СП 256 для общественных зданий коэффициенты спроса (Кс) уже
    учитывают одновременность нагрузки, поэтому дополнительный Ку не
    применяется: P_расч = Σ(P_i × Кс_i).

    Таблица _KU_TABLE сохранена для справки и возможного использования
    в специальных расчётах через прямой вызов _KU_TABLE[n].
    """
    return 1.0


def get_consumer_factors(consumer_type: str) -> dict:
    """Возвращает {"ks": ..., "cos_phi": ...} для типа потребителя."""
    return _CONSUMER_FACTORS.get(consumer_type, _CONSUMER_FACTORS["default"])
